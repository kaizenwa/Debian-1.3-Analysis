#include "e_card.h"
#include "u_cardst.h"
/*--------------------------------------------------------------------------*/
//	void	CARDSTASH::operator=(CARD*);
//	void	CARDSTASH::restore();
/*--------------------------------------------------------------------------*/
void CARDSTASH::operator=(CARD* b)
{
  brh = b;
  value = b->val;
  x = b->x;
}
/*--------------------------------------------------------------------------*/
void CARDSTASH::restore()
{
  brh->val = value;
  brh->x = x;
}
