//=========================>>> GeneratedBy <<<==========================
  static void GeneratedBy(ofstream& of, char* comment)
  {
    char buff[40];
    char date[20];
    vGetLocalTime(buff);
    vGetLocalDate(date);
    strcat(buff," ");
    strcat(buff,date);

of << comment << "=======================================================================\n";
of << comment << "@V@:Note: This file generated by vgen V" << VGEN_VERS << " ";
of << "(" << buff << ").\n";
  }

